// JavaScript Basic Syntax Examples

// Variables
var name = "John";
let age = 25;
const PI = 3.14;

// Operators
let x = 5;
let y = 10;
let sum = x + y;
let product = x * y;
let isGreater = x > y;

// Data Types
let message = "Hello, World!";
let count = 10;
let isTrue = true;
let fruits = ["apple", "banana", "orange"];

// Console Output
console.log("Name:", name);
console.log("Age:", age);
console.log("PI:", PI);
console.log("Sum:", sum);
console.log("Product:", product);
console.log("Is Greater:", isGreater);
console.log("Message:", message);
console.log("Count:", count);
console.log("Is True:", isTrue);
console.log("Fruits:", fruits);